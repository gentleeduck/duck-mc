#!/usr/bin/env node
// Project-local CLI for @gentleduck/md. Speaks `duck-md.config.{ts,js}`.

import { spawnSync } from 'node:child_process'
import { createHash } from 'node:crypto'
import { existsSync, mkdirSync, readdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs'
import { createRequire } from 'node:module'
import { dirname, isAbsolute, join, resolve } from 'node:path'
import { fileURLToPath, pathToFileURL } from 'node:url'

import { build } from '@gentleduck/md'

const CONFIG_CANDIDATES = [
  'duck-md.config.ts',
  'duck-md.config.mts',
  'duck-md.config.js',
  'duck-md.config.mjs',
]

function parseArgs(argv) {
  const args = { _: [], flags: {} }
  let i = 0
  while (i < argv.length) {
    const a = argv[i]
    if (a.startsWith('--')) {
      const eq = a.indexOf('=')
      if (eq !== -1) {
        args.flags[a.slice(2, eq)] = a.slice(eq + 1)
      } else {
        const next = argv[i + 1]
        if (next && !next.startsWith('-')) {
          args.flags[a.slice(2)] = next
          i++
        } else {
          args.flags[a.slice(2)] = true
        }
      }
    } else if (a.startsWith('-')) {
      args.flags[a.slice(1)] = true
    } else {
      args._.push(a)
    }
    i++
  }
  return args
}

function resolveConfigPath(cwd, explicit) {
  if (explicit) {
    const abs = isAbsolute(explicit) ? explicit : resolve(cwd, explicit)
    if (!existsSync(abs)) throw new Error(`config not found: ${abs}`)
    return abs
  }
  for (const name of CONFIG_CANDIDATES) {
    const p = resolve(cwd, name)
    if (existsSync(p)) return p
  }
  throw new Error(
    `no config found in ${cwd}; expected one of: ${CONFIG_CANDIDATES.join(', ')}`,
  )
}

async function loadConfig(configPath) {
  // tsx's programmatic register() leaves extension-less relative
  // imports broken under CJS resolution, so delegate to the tsx CLI
  // by re-exec'ing the bin instead of registering inline.
  if (/\.m?ts$/.test(configPath) && !process.env.DUCK_MD_TSX) {
    reexecUnderTsx()
  }
  const mod = await import(`${pathToFileURL(configPath).href}?t=${Date.now()}`)
  return mod.default ?? mod
}

function reexecUnderTsx() {
  // tsx >=4 hides `dist/cli.mjs` behind `package.json#bin` instead of
  // exporting it as a subpath; resolve via the package.json directly.
  let tsxBin
  for (const from of [pathToFileURL(`${process.cwd()}/`).href, import.meta.url]) {
    try {
      const pkgPath = createRequire(from).resolve('tsx/package.json')
      const pkg = JSON.parse(readFileSync(pkgPath, 'utf8'))
      const binRel = typeof pkg.bin === 'string' ? pkg.bin : pkg.bin?.tsx
      if (binRel) {
        tsxBin = join(dirname(pkgPath), binRel)
        break
      }
    } catch {}
  }
  if (!tsxBin) {
    throw new Error('TypeScript config detected but `tsx` is not installed. `npm i -D tsx` or use a .js / .mjs config.')
  }
  const result = spawnSync(
    process.execPath,
    [tsxBin, fileURLToPath(import.meta.url), ...process.argv.slice(2)],
    { stdio: 'inherit', env: { ...process.env, DUCK_MD_TSX: '1' } },
  )
  process.exit(result.status ?? 1)
}

const COLOR = !process.env.NO_COLOR && process.stdout.isTTY
const paint = (n) => (s) => (COLOR ? `\x1b[${n}m${s}\x1b[0m` : s)
const red = paint('31;1')
const yellow = paint('33;1')
const blue = paint('34;1')
const cyan = paint('36;1')
const dim = paint('2')
const bold = paint('1')

const SEVERITY_PAINT = {
  bug: red,
  error: red,
  warning: yellow,
  help: cyan,
  note: blue,
}

function formatDiagnostic(d) {
  const head = `${(SEVERITY_PAINT[d.severity] ?? dim)(d.severity)}${dim('[')}${bold(d.code)}${dim(']')}: ${d.message}`
  const lines = [head]
  if (d.file) {
    const loc = d.line ? `${d.file}:${d.line}${d.column ? `:${d.column}` : ''}` : d.file
    lines.push(`  ${dim('-->')} ${loc}`)
  }
  if (d.help) lines.push(`  ${cyan('=')} ${bold('help')}: ${d.help}`)
  return lines.join('\n')
}

function flushDiagnostics(report) {
  const diagnostics = report?.diagnostics ?? []
  if (!diagnostics.length) return
  console.log(`[duck-md] ${diagnostics.length} diagnostic${diagnostics.length === 1 ? '' : 's'}:`)
  for (const d of diagnostics) {
    const stream = d.severity === 'error' || d.severity === 'bug' ? process.stderr : process.stdout
    stream.write(`${formatDiagnostic(d)}\n`)
  }
}

function writeCollectionIndex(config, outDir) {
  const cfgCollections = config.collections ?? {}
  const entries = Object.entries(cfgCollections)
    .map(([key, c]) => ({ key, name: c?.name ?? key }))
    .sort((a, b) => a.key.localeCompare(b.key))

  const indexJs =
    '// Generated by duck-md build\n' +
    entries
      .map((e) => `export { default as ${e.key} } from './${e.name}.json' with { type: 'json' }`)
      .join('\n') +
    '\n'

  const indexDts =
    '// Generated by duck-md build\n' +
    "import type __vc from '../duck-md.config'\n\n" +
    "type Collections = (typeof __vc)['collections']\n\n" +
    entries
      .map((e) => {
        const Cap = e.key.charAt(0).toUpperCase() + e.key.slice(1)
        return (
          `export type ${Cap} = NonNullable<Collections['${e.key}']['schema']>['_output']\n` +
          `export declare const ${e.key}: ${Cap}[]\n`
        )
      })
      .join('\n') +
    '\n'

  if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true })
  writeFileSync(join(outDir, 'index.js'), indexJs)
  writeFileSync(join(outDir, 'index.d.ts'), indexDts)
  return entries.length
}

async function runBuild({ cwd, configPath, quiet }) {
  const config = await loadConfig(configPath)
  const outDir = resolve(cwd, config.output?.data ?? '.gentleduck')

  if (!quiet) {
    console.log(`[duck-md] building -> ${outDir}`)
    console.log('[duck-md] collections:', Object.keys(config.collections ?? {}))
  }

  const started = Date.now()
  const report = await build(config)
  flushDiagnostics(report)

  if (report?.errors?.length) {
    for (const e of report.errors) console.error(`  ${red('!')} ${e.file}: ${e.message}`)
    return { ok: false, report, config, outDir }
  }

  const collections = report?.collections ?? []
  if (collections.length) {
    const count = writeCollectionIndex(config, outDir)
    if (!quiet) {
      for (const c of collections) {
        console.log(`  ${c.name}: ${c.records} records -> ${c.outputPath}`)
      }
      console.log(`[duck-md] wrote ${count} collection index entries in ${Date.now() - started}ms`)
    }
  } else if (!quiet) {
    console.log('[duck-md] nothing to write (no collections)')
  }
  return { ok: true, report, config, outDir }
}

async function cmdBuild(args) {
  const cwd = resolve(args.flags.cwd ?? process.cwd())
  const configPath = resolveConfigPath(cwd, args.flags.config)
  const r = await runBuild({ cwd, configPath, quiet: !!args.flags.quiet })
  process.exit(r.ok ? 0 : 1)
}

async function cmdInfo(args) {
  const cwd = resolve(args.flags.cwd ?? process.cwd())
  const configPath = resolveConfigPath(cwd, args.flags.config)
  const config = await loadConfig(configPath)
  const outDir = resolve(cwd, config.output?.data ?? '.gentleduck')
  console.log(`config:     ${configPath}`)
  console.log(`root:       ${resolve(cwd, config.root ?? '.')}`)
  console.log(`output:     ${outDir}`)
  const cols = config.collections ?? {}
  console.log(`collections: ${Object.keys(cols).length}`)
  for (const [key, c] of Object.entries(cols)) {
    console.log(`  - ${key} (name=${c?.name ?? key}, pattern=${c?.pattern ?? '?'})`)
  }
}

async function cmdClean(args) {
  const cwd = resolve(args.flags.cwd ?? process.cwd())
  const configPath = resolveConfigPath(cwd, args.flags.config)
  const config = await loadConfig(configPath)
  const outDir = resolve(cwd, config.output?.data ?? '.gentleduck')
  const cacheDir = join(outDir, '.cache')
  if (existsSync(cacheDir)) {
    rmSync(cacheDir, { recursive: true, force: true })
    console.log(`[duck-md] removed ${cacheDir}`)
  } else {
    console.log(`[duck-md] nothing to remove (no ${cacheDir})`)
  }
}

function hashFile(absPath) {
  try {
    return createHash('sha256').update(readFileSync(absPath)).digest('hex')
  } catch {
    return null
  }
}

function isContentFile(p) {
  return p.endsWith('.md') || p.endsWith('.mdx')
}

function walkContentFiles(root) {
  const out = []
  const stack = [root]
  while (stack.length) {
    const dir = stack.pop()
    let entries
    try {
      entries = readdirSync(dir, { withFileTypes: true })
    } catch {
      continue
    }
    for (const ent of entries) {
      const p = join(dir, ent.name)
      if (ent.isDirectory()) {
        if (ent.name === 'node_modules' || ent.name === '.gentleduck' || ent.name === '.dmc-cache' || ent.name.startsWith('.git')) continue
        stack.push(p)
      } else if (ent.isFile() && isContentFile(p)) {
        out.push(p)
      }
    }
  }
  return out
}

async function cmdDev(args) {
  const cwd = resolve(args.flags.cwd ?? process.cwd())
  let configPath = resolveConfigPath(cwd, args.flags.config)
  const debounce = Number(args.flags.debounce ?? 100)

  let chokidar
  try {
    ;({ default: chokidar } = await import('chokidar'))
  } catch {
    throw new Error('dev mode needs `chokidar`. Install it (`npm i -D chokidar`).')
  }

  let { config } = await runBuild({ cwd, configPath, quiet: false })

  const root = resolve(cwd, config.root ?? '.')

  // Content-hash map deduplicates no-op saves (touch / save without edit).
  // chokidar fires on mtime change; without this we re-walk the entire tree
  // and re-emit every collection JSON for a file that didn't actually change.
  const hashes = new Map()
  for (const f of walkContentFiles(root)) {
    const h = hashFile(f)
    if (h) hashes.set(resolve(f), h)
  }
  // Track config hash too so touch on duck-md.config.* without edit also no-ops.
  let configHash = hashFile(configPath)

  const watcher = chokidar.watch([root, configPath], {
    ignoreInitial: true,
    ignored: (p) =>
      p.includes(`${'.gentleduck/.cache'}`) ||
      p.endsWith('~') ||
      /\.(swp|swo)$/.test(p),
  })

  console.log(`[duck-md] watching ${root} (${hashes.size} tracked files)`)

  let timer = null
  let pendingConfigChange = false
  const rebuild = async () => {
    timer = null
    try {
      if (pendingConfigChange) {
        pendingConfigChange = false
        configPath = resolveConfigPath(cwd, args.flags.config)
        configHash = hashFile(configPath)
      }
      const r = await runBuild({ cwd, configPath, quiet: false })
      config = r.config
    } catch (e) {
      console.error(`[duck-md] dev rebuild failed: ${e.message ?? e}`)
    }
  }
  const schedule = () => {
    if (timer) clearTimeout(timer)
    timer = setTimeout(rebuild, debounce)
  }

  watcher.on('all', (evt, p) => {
    const abs = resolve(p)

    // Config: rebuild only if contents changed; track new hash either way.
    if (abs === configPath) {
      if (evt === 'unlink') {
        configHash = null
        pendingConfigChange = true
        schedule()
        return
      }
      const next = hashFile(abs)
      if (next && next === configHash) {
        console.log(`[duck-md] no-op (config saved, unchanged)`)
        return
      }
      configHash = next
      pendingConfigChange = true
      schedule()
      return
    }

    // Add/unlink for any path: rebuild (collection membership may shift).
    if (evt === 'add' || evt === 'unlink' || evt === 'addDir' || evt === 'unlinkDir') {
      if (evt === 'unlink') {
        hashes.delete(abs)
      } else if (evt === 'add' && isContentFile(abs)) {
        const h = hashFile(abs)
        if (h) hashes.set(abs, h)
      }
      schedule()
      return
    }

    // Change event. For content files, dedupe by content hash; for other
    // files in root, fall through and rebuild (could affect plugins/inputs).
    if (isContentFile(abs)) {
      const next = hashFile(abs)
      if (!next) {
        schedule()
        return
      }
      const prev = hashes.get(abs)
      if (prev === next) {
        console.log(`[duck-md] no-op (${relativeFrom(root, abs)} unchanged)`)
        return
      }
      hashes.set(abs, next)
      schedule()
      return
    }

    schedule()
  })

  process.on('SIGINT', () => {
    console.log('\n[duck-md] stopping')
    watcher.close().finally(() => process.exit(0))
  })
}

function relativeFrom(root, abs) {
  if (abs.startsWith(`${root}/`)) return abs.slice(root.length + 1)
  return abs
}

function printHelp() {
  process.stdout.write(`duck-md - build / watch a duck-md.config project

Usage:
  duck-md build [--config <path>] [--cwd <dir>] [--quiet]
  duck-md dev   [--config <path>] [--cwd <dir>] [--debounce <ms>]
  duck-md info  [--config <path>] [--cwd <dir>]
  duck-md clean [--config <path>] [--cwd <dir>]
  duck-md --help | --version

Config search order (no --config): ${CONFIG_CANDIDATES.join(', ')}
`)
}

async function main() {
  const args = parseArgs(process.argv.slice(2))
  if (args.flags.help || args.flags.h) return printHelp()
  if (args.flags.version || args.flags.v) {
    const pkg = createRequire(import.meta.url)('../package.json')
    console.log(pkg.version)
    return
  }
  const cmd = args._[0] ?? 'build'
  switch (cmd) {
    case 'build':
      return cmdBuild(args)
    case 'dev':
    case 'watch':
      return cmdDev(args)
    case 'info':
      return cmdInfo(args)
    case 'clean':
      return cmdClean(args)
    default:
      console.error(`unknown command: ${cmd}`)
      printHelp()
      process.exit(2)
  }
}

main().catch((e) => {
  console.error(`[duck-md] ${e?.stack ?? e?.message ?? e}`)
  process.exit(1)
})
